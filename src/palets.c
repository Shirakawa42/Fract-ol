#include "../includes/fractol.h"

int		palet(t_mlx *truc)
{
	int		i;

	i = truc->i;
	while (i > 20)
		i = i / 2;
	if (truc->s == 2)
		return (i * 0x004488BA);
	if (truc->s == 3)
		return (i * 0x006677BB);
	return (i * 0x005544AA);
}
